RandomLevelTemplate::RandomLevelTemplate(QWidget *parent, QPixmap *bgImg, int index, Game *game)
    : QWidget{parent}
{
    Q_UNUSED(game);

    this->setGeometry(parent->rect());
    ind = index;
    tabs = new QStackedWidget(this);
    tabs->setGeometry(parent->rect());

    currentTask = rand() % randomTasks[index - 100].size();

    mainTab = new RandomLevelMainTab(this);
    mainTab->setGeometry(parent->rect());
    mainTab->setBackgroundImage(bgImg);

    terminalTab = new RandomLevelTerminal(this, index);
    terminalTab->setGeometry(parent->rect());
    terminalTab->setBackgroundImage(new QPixmap(":/images/terminalBg.png"));
    terminalTab->setCurrentTask(currentTask);

    hintTab = new RandomLevelHint(this, index);
    hintTab->setGeometry(parent->rect());
    hintTab->setBackgroundImage(new QPixmap(":/images/levelHintBg.png"));

    tabs->addWidget(mainTab);
    tabs->addWidget(terminalTab);
    tabs->addWidget(hintTab);

    tabs->setCurrentWidget(mainTab);
}

RandomLevelTemplate::~RandomLevelTemplate(){

}

void RandomLevelTemplate::setBackgroundImage(QPixmap *img){
    backgroundImage = img;
}

void RandomLevelTemplate::goMainTab(){
    tabs->setCurrentWidget(mainTab);
}

void RandomLevelTemplate::goTerminal(){
    tabs->setCurrentWidget(terminalTab);
}

void RandomLevelTemplate::goHint(){
    tabs->setCurrentWidget(hintTab);
}

void RandomLevelTemplate::checkComplete(){
    QString s1 = terminalTab->getText(), s2 = QString::fromStdString(randomTasks[ind - 100][currentTask].second);

    s1 = s1.toLower();
    s2 = s2.toLower();

    s1 = s1.trimmed();
    s2 = s2.trimmed();

    s1.replace(" ", "");
    s2.replace(" ", "");

    if (s1 == s2){
        isCompleted = true;
        terminalTab->setCompleted();
        mainTab->setCompleted();
    }
}

void RandomLevelTemplate::roll() {
    int newOne = currentTask;

    while (newOne == currentTask)
        newOne = rand() % randomTasks[ind - 100].size();

    currentTask = newOne;

    terminalTab->setCurrentTask(currentTask);
}

bool RandomLevelTemplate::getCompleted(){
    return isCompleted;
}


